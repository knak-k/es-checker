// ES添削のプロンプト（サーバーAPI・ブラウザ内モデルの両方で共有）。
// plan.md の採点ルーブリック（5項目×20点／減点チェック／辛口）を反映。

export const REVIEW_SYSTEM =
  "あなたは就活ES（エントリーシート）の辛口添削者です。" +
  "日本語で、結論ファーストで、指定フォーマットに厳密に従って簡潔に出力してください。" +
  "初稿で80点超は稀です。抽象語（信頼・成長・挑戦など）の未定義使用、数字偏重、" +
  "感情語での価値補強、善意のみで仕組み設計が欠けている点は減点対象として指摘してください。";

export type ReviewInput = {
  company: string;
  role: string;
  question: string;
  body: string;
};

export function buildReviewPrompt(p: ReviewInput): string {
  return [
    "以下のESを添削してください。応募職種に応じた視点で評価してください。",
    "",
    `企業: ${p.company || "(未記入)"}`,
    `職種: ${p.role || "(未記入)"}`,
    `設問: ${p.question || "(未記入)"}`,
    "本文:",
    p.body,
    "",
    "出力フォーマット（この見出しを厳守）:",
    "【総合評価】100点満点で採点（辛口）＋2〜3文の総評",
    "【項目別採点】次の5項目を各20点で採点し、各項目「得点／評価理由／不足要素／具体的改善案」を1〜2行で:",
    "  主体性 / 思考力 / 実行力 / 他者理解力 / 再現性",
    "【減点チェック】数字偏重・感情語・抽象語の未定義使用・仕組み設計の欠如などが該当すれば指摘",
    "【優先改善】最も効く改善を3つ、具体的に",
  ].join("\n");
}
